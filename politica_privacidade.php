<br />

Para você se tornar um usuário registrado, fazer compras, ou vender conteúdo através de nossos sites, você vai precisar nos fornecer algumas informações pessoais, como: idade, nome, nome da empresa, se for o caso, endereço de e-mail, rua, endereço e número de telefone durante o processo de registro. Para compras on-line ou de vendas, também vai exigir o número do seu cartão crédito ou de débito, data de validade, endereço de cobrança e código de segurança, a fim de processar os pagamentos. Podemos também pedir informações pessoais adicionais de tempo em tempo, tais como: gênero e nível de renda, em relação a promoções on-line, concursos, brindes, pesquisas e outros programas, mas é inteiramente você quem deve ou não fornecer essas informações para participar desses programas.
