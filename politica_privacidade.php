QUAIS AS INFORMAÇÕES QUE COLETAMOS?
<br />

Nós geralmente recolhemos dois tipos de informação através do site: "informação pessoal" sobre você, que você fornecer para nós, e "informações não pessoais" sobre você, seu computador e da tecnologia, e seu uso dos Sites, que por si só não o identificam pessoalmente.
