<br />

Nós também podemos usar suas informações pessoais e não pessoais para melhorar sua experiência com os Sites e nosso serviço, como o envio de e-mails e fornecendo-lhe de outra forma com outras informações, conteúdo e material em que pensamos que poderá estar interessado, exibindo conteúdo e anúncios de acordo com o seu histórico de navegação antes, preferências e localização; cumprimento de suas ordens; prestação de serviço ao cliente e responder às suas perguntas e comentários, ou entrar em contato com você sobre sua conta ou sobre o nosso e promoções dos parceiros, concursos, pesquisas, ou outras promoções.
