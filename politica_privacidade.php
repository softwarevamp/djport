USAMOS "COOKIES" OU "WEB BEACONS"?
<br />

Nós usamos "cookies" para coletar dados de uso do site e para melhorar sua experiência nos Sites e nosso serviço em geral. Um "cookie" é um pequeno arquivo de dados transferidos para o computador para acesso em futuras visitas ao site, o que pode nos dizer que você (ou alguém usando o mesmo computador ou o endereço IP) visitou nosso site (s) antes. Você pode parar de aceitar cookies, basta configurar o seu navegador de Internet para isso, mas provavelmente irá resultar em não reconhecer as suas preferências em visitas futuras, de modo que você pode ter que re-introduzir determinadas informações.
<br />
<br />

Os sites e e-mails de nós podem conter imagens eletrônicas conhecidas como "web beacons". Estes são os arquivos que nos permitem contar os visitantes de nossos sites, para dizer se um anúncio foi visto ou se um e-mail foi aberto ou encaminhados, ou para servir de conteúdo ou serviços. Web beacons coletar informações não-pessoais limitado, incluindo o número do farol, data e hora de uma página ou e-mail vista, e uma descrição da página em que o Web beacon reside, tudo o que podemos armazenar e usar. A desativação dos cookies pode impedir alguma atividade balizas ", mas geralmente as balizas funcionará se você visitar os sites ou abrir e-mails de nós.
<br />
