Seus comentários e outras informações também serão usadas para avaliar e melhorar os nossos sites e serviços. Por exemplo, podemos analisar o tráfego e uso do nosso site, a taxa de participação de nossas várias promoções, ofertas, e-mails e outros programas; e a popularidade do nosso conteúdo, artistas e fornecedores participantes. Também podemos utilizar essa informação para fins de desenvolvimento de produtos, tais como determinar o que outras gravadoras, artistas, produtos e serviços de nossos usuários estão interessados, e para fins técnicos, tais como monitoramento da funcionalidade dos nossos servidores e sistemas de email.
