SUAS INFORMAÇÕES SÃO SEGURAS?
<br />

Nós empregamos medidas administrativas, físicas e eletrônicas destinadas a proteger e livrar suas informações de tentativas de acesso não autorizado. Todas as operações de crédito ocorrem em uma área segura fora do site (Todo pagamento é feito no site do Paypal), usando Secure Sockets Layer (SSL) para processar pedidos, que criptografa as informações introduzidas fora do site, essas informações são protegidas pelo Paypal.
<br />
<br />

Além disso, todas as informações pessoais do usuário são armazenados atrás de um firewall com acesso administrativo limitado. No entanto, lembre-se de que, apesar de nossos melhores esforços, nenhuma medida de segurança é perfeita ou impenetrável.
<br />
<br />

Qualquer informação ou material que você pode expor na parte visível publicamente do Djport, como em uma página de perfil de DJ, um fórum online ou comentário, sala de bate-papo, ou quadro de avisos, será facilmente acessível ao público em geral. Por isso, não entram nas áreas do site qualquer informação pessoal (por exemplo, seu nome, e-mail ou endereço postal, cartão de crédito) ou outras informações ou materiais que você não quer que esteja publicamente disponível. Isto também se aplica a qualquer comunicação que você pode ter diretamente com outros usuários dos sites.
