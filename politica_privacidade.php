QUE TIPO DE "COOKIES" QUE VAMOS USAR?
<br />

Com base nas diretrizes encontradas no ICC UK Cookie Guide, podemos usar as seguintes categorias:
<br />
<br />

OS COOKIES ESTRITAMENTE NECESSÁRIOS
<br />

Esses cookies são essenciais para permitir que você possa navegar em nosso site e usar seus recursos. Sem esses cookies, serviços como carrinhos de compras e pagamentos eletrônicos não podem ser fornecidos. Para estes tipos de cookies que são estritamente necessários, o consentimento do usuário não é necessário.
<br />
<br />

COOKIES DE DESEMPENHO
<br />

Estes cookies coletam informações anônimas sobre como os visitantes utilizam o nosso site e se deparam com mensagens de erro de nossas páginas web. Toda a informação recolhida é agregada e, portanto anônimos. Eles são usados apenas para melhorar a forma como o site funciona. Ao utilizar nosso site, você concorda que podemos colocar estes tipos de cookies em seu dispositivo.
<br />
<br />

OS COOKIES DE FUNCIONALIDADE
<br />

Estes cookies permitem que nossos sites lembrem escolhas que você faz durante a navegação, como a linguagem, tamanho da fonte, e as preferências de nome de usuário. Podemos também lembrar preferências, como tamanho de texto, fontes e outros elementos do site personalizáveis. Eles também podem ser usados para permitir que você assista a vídeos ou comentários em um blog. A informação coletadas nesses cookies não vai identificá-lo pessoalmente, e eles não podem controlar sua atividade de navegação fora de nossos sites. Quando você escolher essas preferências, você concorda que podemos colocar esses cookies de personalização funcionais no seu dispositivo.
<br />
<br />

COOKIES DE PUBLICIDADE
<br />

Esses cookies são usados para entregar publicidade segmentada como pode ser relevante para você e seus interesses. Eles também são usados para limitar o número de vezes que você vê um anúncio e ajudar a medir a eficácia da campanha. Eles se lembram de ter visitado um site e é compartilhado com outros sites, como anunciantes. Podem estar ligados a funcionalidade fornecida por outros locais. Você pode escolher se quer permitir que esses cookies alterando suas configurações de cookies no seu browser.
<br />
