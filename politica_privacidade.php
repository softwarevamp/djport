Podemos usar as informações aprendidas a partir dos cookies e web beacons para ter certeza de que o nosso conteúdo e e-mails estão em um formato que você pode ler, analisar tendências sobre que sites de conteúdo, e que e-mails são populares e estão sendo lidos e/ou encaminhados, para manter um histórico de atividade de email, e para avaliar a eficácia e eficiência de determinadas comunicações.
