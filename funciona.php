O TOP10 E O TOP100
<br />

O Funcionamento do top 10/top 100 se dá da seguinte forma: Cada track/djset postado no Djport recebe um sistema interno de pontuação, cada ponto equivale a um download, então quanto mais downloads suas tracks/djsets tiverem, mais pontos eles ganham, quanto mais pontos, melhor a colocação no top10/top100.
Seus arquivos postados no Djport têm grande chance de aparecer nos tops, pois cada gênero tem seu próprio top10/top100.
<br />

Faça o teste comparativo, anote a quantidade de plays e de download que sua faixa tem antes de colocar no Djport e  depois de postá-la no site, assim você consegue mensurar o resultado junto conosco e vai chegar a conclusão que o Djport é um ótimo canal de divulgação do seu trabalho. 

