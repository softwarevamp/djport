COMO FAZER UPLOAD
<br />

O DJport é um site de downloads de música eletrônica gratuito, onde quem paga são os artistas que querem divulgar seus trabalhos no portal.
<br />

Siga os passos para saber como colocar suas tracks/djsets no site:
<br />

1.	Para postar suas tracks ou djsets no Djport, primeiramente você deve fazer seu cadastro.
<br />

2.	Após realizar seu cadastro, escolha um plano e clique em assinar, você terá duas semanas grátis para usar e testar o site;
<br />

3.	Para fazer o ulpoad de seus arquivos, faça seu login, clique no botão upload, selecione em seu computador um arquivo de áudio em formato mp3, no caso de tracks a qualidade precisa estar em 320kbps e no caso de djsets, a qualidade deve estar entre 128kbps e 320kbps, o tamanho do arquivo deve ser no máximo 200MB.
<br />

4.	Marque uma das opções: Track ou Djset, depois escolha um gênero.
<br />

5.	Selecione em seu computador uma imagem para a capa da música, tudo pronto. 
<br />

Depois que suas tracks/djsets estiverem no ar, elas estarão disponíveis para downloads gratuitos para todo o público do site. 
