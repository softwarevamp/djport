2. O QUE É UM "ORIGINAL MIX"?
<br />

Como a própria nomenclatura já diz, original mix, é a primeira edição de uma música, é a forma original como ele foi produzida sem nenhuma modificação, seja ela por parte de seu produtor original ou seja por um segundo ou terceiro.
<br />
<br />

3. O QUE É UM "REMIX"?
<br />

Um remix é uma música que foi editada para parecer diferente da versão original. A pessoa que faz o remix pode por exemplo, mudar o tom de voz dos cantores, mudar o ritmo e velocidade e fazer a música mais curta ou mais longa. Geralmente um remix é feito em cima dos arquivos midi originais, onde esses arquivos são editados e sofrem mudanças de timbre, tempo, tom e etc. São também acrescidos elementos, efeitos ou até mesmo diminuídos.
<br />
<br />

4. O QUE É UM "DJSET"?
<br />

É uma compilação de uma lista de músicas mixadas por um dj. As faixas são selecionadas, é feita a sincronia do bpm e são mixadas uma na outra sem intervalo de tempo e com os beats nivelados. Os djsets podem ser feitos usando cdj/toca discos e mixer ou em softwares de edição de áudio no computador.
<br />
<br />

5. O QUE É UM "MASHUP"/ "BOOTLEG"?
<br />

Um mashup ou bootleg é uma canção ou composição criada pela mistura de duas ou mais músicas pré-gravadas, normalmente através da sobreposição da faixa vocal de um canção perfeitamente sobre a trilha instrumental de outra. Na medida em que essas obras são "transformadoras" de conteúdo original, elas podem encontrar proteção contra reivindicações de direitos autorais sob o "uso justo", doutrina da lei de direitos autorais.
<br />
<br />

6. O QUE É UM "EP"?
<br />
Extended play (EP) é uma gravação em vinil ou CD que é longa demais para ser considerada um compacto (single), e muito curta para ser classificada como álbum. Normalmente um álbum tem oito ou mais faixas e tem duração variando entre trinta e sessenta minutos. Um compacto tem de 01 a 06 faixas e geralmente tem uma duração típica de dois a quinze minutos. Um EP tem entre duas e oito faixas (somando os dois lados de um EP de Vinil ou o total de Faixas de Compact Disc EP Single) e duração de 03 a 40 minutos.
<br />
<br />
Alguns artistas preferem chamar um EP de mini-álbum para dar um significado maior ao seu trabalho em vez de ser classificado apenas como mais um aditivo em sua discografia.
<br />
<br />

7. O QUE É UM "SINGLE"?
<br />

Possui no Máximo quatro faixas com a música em sua versão original e geralmente em Radio Edit (cortada), Extended (mais prolongada) e dois Remixes, além de um B-Side (faixa exclusiva do Single).
<br />
<br />

8. O QUE É UM "ALBUM"?
<br />

Um álbum musical é uma coleção de músicas relacionadas, lançadas juntas comercialmente em um formato de áudio para o público. As faixas do álbum normalmente estão relacionadas quanto a tema e estilo musical, mas isso não é uma regra. Um álbum pode ser lançado em diversas mídias diferentes, desde mídias "físicas", como CD, DVD áudio, fita cassette e LP, até mídias "digitais", como arquivos MP3 e AAC.
<br />
<br />


9. O QUE É UM "SELO"/ "LABEL"?
<br />
O nome selo é uma tradução de label, termo em inglês que designa divisões dentro de uma gravadora. O labels geralmente cuidam de estilos musicais de gravadoras que lançam de tudo, como a Universal e a EMI, que têm diversos selos para cada estilo. Muitos selos não fazem parte de gravadoras e foram criados para o lançamento de músicas de forma independente.
<br />
<br />
Na música eletrônica a maioria dos selos são a própria "gravadora", mas alguns fazem parte de grupos maiores. Os selos costumam se especializar em estilos musicais como techno, house e electro, porém hoje é muito comum vermos selos especializados em sub-gêneros, como electro-house, deep-house, nu-disco, entre outros.
<br />
<br />

Outra coisa muito comum na música eletrônica são os próprios artistas abrirem selos para divulgar seus trabalhos. Mas cuidar de um selo exige dedicação, contatos, gastos e conhecimento do mercado e suas tendências.
<br />
<br />

10. O QUE É UMA GRAVADORA?
<br />
A gravadora é uma marca associada com a comercialização de gravações de músicas e vídeos de música . Muitas vezes, uma gravadora é também uma empresa que gerencia tais marcas e; coordena a produção, fabricação, distribuição, comercialização, promoção e execução de direitos autorais de gravações sonoras e vídeos musicais; realiza talento de aferição e desenvolvimento de novos artistas ("artistas e repertório "ou" A & R "), e mantém contratos com artistas e seus gerentes. O termo "selo" deriva da etiqueta redonda no centro de um vinil, registro que exibe com destaque o nome do fabricante, junto com outras informações.
<br />
